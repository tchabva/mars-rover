package org.northcoders.inputlayer;

public class PlateauSize {
    private final int x;
    private final int y;

    public PlateauSize(int y, int x) {
        this.y = y;
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public int getX() {
        return x;
    }
}
